# =========== casting ============= #

# Python is an object-orientated language, and as such it uses classes to define data types, including its primitive types

a = int(2)   # will be 2
b = int(2.5) # will be 2
c = int('2') # will be 2

# we can use methods to define types
string = str(123)      # '123'
integer = int('123')   # 123
float = float('123.4') # 123.4
